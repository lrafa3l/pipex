int	ft_count_words(char *s)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (s[i])
	{
		if ((s[i] >= 33 && s[i] <= 126) && (s[i + 1] == 32 || s[i + 1] == '\0'
				|| (s[i + 1] >= 9 && s[i + 1] <= 13)))
			count++;
		i++;
	}
	return (count);
}
