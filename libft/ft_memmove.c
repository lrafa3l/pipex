#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*sr;
	char	*ds;

	i = n;
	ds = (char *)dest;
	sr = (char *)src;
	if (dest == src)
		return (dest);
	else if (ds > sr)
	{
		while (i > 0)
		{
			i--;
			ds[i] = sr[i];
		}
	}
	else
		ft_memcpy(dest, src, n);
	return (ds);
}
